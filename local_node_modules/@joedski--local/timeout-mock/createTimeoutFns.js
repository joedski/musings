module.exports = function createTimeoutFns() {
  const $$ = {
    id: 0,
    pending: [],
    setTimeout(fn, ms, ...args) {
      const entry = {
        id: ++$$.id,
        ms,
        fn,
        args,
      };
      $$.pending.push(entry);
      return entry.id;
    },
    clearTimeout(id) {
      const entryIndex = $$.pending.findIndex(e => e.id === id);
      if (entryIndex) {
        $$.pending.splice(entryIndex, 1);
      }
    },
    advanceTime(ms) {
      const expiredIndices = [];
      $$.pending.forEach((entry, i) => {
        entry.ms -= ms;
        if (entry.ms <= 0) {
          expiredIndices.push(i);
          entry.fn(...entry.args);
        }
      });
      expiredIndices.reverse().forEach(i => {
        $$.pending.splice(i, 1);
      });
    },
  };

  return $$;
}
